`timescale 1ns/1ps  // 时间单位 1ns，精度 1ps

module tb_dds_system();

    // 1. 信号定义：与你原理图的输入输出引脚严格对应
    reg         CLK;          // 对应你图中的 CLK
    reg  [23:0] step;         // 对应你图中的 step[23..0]
    wire [7:0]  out;          // 对应你图中的 out[7..0]

    // 2. 实例化顶层模块 (UUT: Unit Under Test)
    // ⚠️ 请把 "top_level_name" 换成你当前 BDF 原理图的名字！
    changable uut (
        .CLK  (CLK),
        .step (step),
        .out  (out)
    );

    // 3. 产生系统心跳：50MHz 时钟
    // 周期为 20ns (高电平 10ns，低电平 10ns)
    initial begin
        CLK = 0;
        forever #10 CLK = ~CLK; 
    end

    // 4. 核心激励：模拟不同频率的控制字 (多段测试)
    initial begin
        // 系统刚上电，先给步长清零，稍微等一下
        step = 24'd0;
        #200;

        // =======================================================
        // 第一段：低频波形（波形舒缓，细节清晰）
        // 设定步长 K = 83886
        // 理论频率：(83886 * 50MHz) / 2^24 ≈ 250 kHz
        // 理论周期：约 4000 ns
        // =======================================================
        step = 24'd83886;
        #20000;  // 运行 20000ns (20微秒)，刚好能完整展示 5 个正弦波周期

        // =======================================================
        // 第二段：中频波形（频率提升 5 倍，波形开始变密）
        // 设定步长 K = 419430
        // 理论频率：(419430 * 50MHz) / 2^24 ≈ 1.25 MHz
        // =======================================================
        step = 24'd419430;
        #20000;  // 再运行 20000ns，这里你能看到大约 25 个周期，明显变密

        // =======================================================
        // 第三段：高频波形（频率再提升 4 倍，波形非常紧凑）
        // 设定步长 K = 1677721
        // 理论频率：(1677721 * 50MHz) / 2^24 ≈ 5 MHz
        // =======================================================
        step = 24'd1677721;
        #20000;  // 再运行 20000ns，满屏都是密集的正弦波

        $stop; // 仿真完美收工！
    end

endmodule